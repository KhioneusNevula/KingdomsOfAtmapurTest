package thinker.concepts.relations.descriptive;

/**
 * The manner in which two profilable things are related
 * 
 * @author borah
 *
 */
public enum PRelationCategory {
	/**
	 * Indicates two things are related by where they are located relative to one
	 * another
	 */
	LOCATION,
	/**
	 * Indicates two things are related by the physical, spiritual, social
	 * connection they have to one another
	 */
	CONNECTION,
	/** Indicates two things are related by the kind of thing they are */
	KIND
}
